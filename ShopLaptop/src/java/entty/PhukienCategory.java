package entty;

/**
 *
 * @author QuangHai144
 */
public class PhukienCategory {
    private int id;
    private String ten;

    public PhukienCategory(int id, String ten) {
        this.id = id;
        this.ten = ten;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    @Override
    public String toString() {
        return "PhukienCategory{" + "id=" + id + ", ten=" + ten + '}';
    }
    
    
}
